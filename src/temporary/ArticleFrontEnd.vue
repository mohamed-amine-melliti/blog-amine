            <p><em>This series of interviews spotlights scientists working across the country to implement the
                    Department of Energy’s massive efforts to transition the country to clean energy, and improve equity
                    and address climate injustice along the way. The Federation’s <a
                        href="https://fas.org/publication/talent-pipeline-for-the-clean-energy-transition/">clean energy
                        workforce report</a> discusses the challenges and opportunities associated with ramping up this
                    dynamic, in-demand workforce. These interviews have been edited for length and do not necessarily
                    reflect the views of the DOE. Discover more DOE spotlights here. </em></p>



            <p>Dr. Rebecca Glaser started her career as an engineer in academia. But her interest in the field’s
                applications for clean energy drove her to take a chance and join the Department of Energy. Now at the
                <strong>Office of Clean Energy Demonstrations</strong>, Dr. Glaser is paving the way for cutting-edge
                energy storage and battery technologies to scale up. With experience in research, commercialization, and
                delivering clean energy directly to communities, Dr. Glaser’s background makes her an exceptional
                example of a clean energy champion.&nbsp;</p>



            <h2>Discovering the Environmental Application of Materials Science</h2>



            <p>Dr. Glaser grew up in the Maryland suburbs of Washington, D.C., and was no stranger to the world of
                public service. Surrounded by an environmentally conscious community, she volunteered throughout high
                school. She had an early interest in math and science, born of a desire to learn more about the world –
                but was not sure how to turn it into a career.&nbsp;</p>



            <p>In her first year of college, Dr. Glaser ended up in a seminar series focused on the technology of energy
                that was full of senior undergraduate and graduate students who were all involved in materials science
                research. Although it was a new field to her, it combined her interest in chemistry and physics with
                obvious applications – sparking her love for that work. “I realized that all of the people teaching [the
                seminar] whose research I found interesting were all in materials science, and most of the energy
                applications I was looking at were being done through that field.”</p>



            <p>But even with a field of study in mind, Dr. Glaser was unsure of where to take her passions. She pursued
                a PhD to dive deeper into batteries and concentrate on one area of technology. “I knew exactly what
                technology I wanted to work on, but I didn’t know where I wanted to put those skills, whether it was
                industry or academia. But I didn’t know government was an option.”</p>



            <h2>Applying the Research</h2>



            <p>In grad school, however, she explored roads less traveled. While peers were doing internships at Intel
                and Tesla, Dr. Glaser applied for a position at <strong>Resources for the Future</strong>, a policy
                research and analysis organization. As part of the internship, she gained insight into how her work was
                connected to real-world issues.&nbsp;</p>



            <p>“We were writing a case study about coal communities that were working through energy transitions – I
                focused on one in <a href="https://fas.org/publication/spotlight-dr-hannah-schlaerth/">Ohio</a>, where
                they were losing or about to lose their coal-fired power plant. We were looking at the effectiveness of
                government intervention.&nbsp; I was interviewing economic development officials in counties across Ohio
                about their experiences with federal grants and the communities that benefit from those programs. All in
                the middle of my very technical battery PhD.”</p>



            <p>It was a valuable experience for Dr. Glaser. When she was finishing her PhD and applying for government
                fellowships, it gave her additional perspectives on how she could use her expertise to make a
                difference.&nbsp;</p>



            <h2>Battery Research and Development at&nbsp; the DOE</h2>



            <p>Dr. Glaser started her work in government as a <strong>ORISE Fellow in DOE’s Solar Energy Technologies
                    Office</strong> (SETO) – maybe on the surface an unlikely choice for someone interested in
                batteries, but not to Dr. Glaser. “You can’t really go forward with solar without energy storage – you
                can only get to a certain point, and I wanted to be that storage expert for them.”&nbsp;</p>



            <p>She credits the experience with giving her a lot of learning opportunities, acting as a resource for
                storage issues, working on program development in topics like recycling, siting, and more. “I learned a
                lot about how government works – all of its intricacies. It gave me a broader appreciation for the
                issues behind the science and really helped direct me towards what I wanted to do next.” </p>



            <p>Dr. Glaser moved into a position as a Project Officer at the <strong>Office of Clean Energy
                    Demonstrations</strong> last March and then to a position as a Project Manager, focusing full-time
                on her passion for energy storage. “It’s an exciting time to be in DOE – it was really cool to graduate
                in 2021 and then have legislation passed that created the office I now work in.” As a project manager,
                she helps steward these new programs, select projects for the office to fund, and support award
                negotiations. There’s a long road ahead, but she is excited for their potential impact.&nbsp;</p>



            <p>OCED handles a wide range of burgeoning clean energy technologies – and Dr. Glaser feels privileged to be
                on the cutting edge of what’s possible in energy storage. “Energy storage is so diverse and interesting
                – I’m excited to see how the different technologies play out and interact with each other, and what I’m
                able to learn about them.” The office has a hefty mandate, but its ability to respond to support the
                energy storage needs of the present as well as the decades to come will make a huge difference in
                achieving a net-zero future.</p>



            <h2>Stiff Competition to Apply Skills that Make Impactful Contributions</h2>



            <p>But an office is only as good as the staff that run it. Too often, the world-class talent that keeps the
                mission going are not recognized for their high-level expertise. Dr. Glaser emphasized that getting to
                support this vital work is because of years of hard work on her part – and that’s one of her biggest
                accomplishments. </p>



            <p>“The transition I was able to make [into government] is a really hard thing to do It’s giving up the
                expected path – to go into industry, into a lab, or into a postdoc.”</p>



            <p>It’s important to note that SETO, the office Dr. Glaser did her fellowship in has a competitive
                application pool. She credits her success making the transition to the work she put in conducting
                informational interviews, taking on work like her internship at Resources for the Future, attending
                conferences – what she calls the “slow systematic work of understanding this new path and how to get
                yourself there.”</p>



            <p>DOE employees like Dr. Glaser put in that effort because they know the potential for impact is so great.
                “I am doing the most I can be doing with my job, with the skill set I have. This is the most impactful
                thing I can do with my skills.”</p>




